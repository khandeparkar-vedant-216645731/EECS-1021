
public class BooleanDataType {

	public static void main(String[] args) {
		// The boolean data type consists of only two values
		// These two values are true and false
		// true and false are the only two possible boolean literals
		boolean p = true;
		boolean q = false;
		System.out.println("p is " + p);
		System.out.println("q is " + q);
		System.out.println("After re-assigning p to false and q to true:");
		p = false;
		q = true;
		System.out.println("p is " + p);
		System.out.println("q is " + q);
	}

}
